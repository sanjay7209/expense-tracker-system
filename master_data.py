from fastapi import Depends, APIRouter
from sqlalchemy.orm import Session 
from database import get_db
from uuid import UUID
import models
import datetime

master_api_router = APIRouter(tags=["Master Data"])


@master_api_router.post("/Country")
async def create_country(db: Session = Depends(get_db)):
    for value in countries:
        country = models.Country(name=value['Country'], code=value['Code'])
        db.add(country)
        db.commit()
        db.refresh(country)

    return {"message": "Countries created successfully"}


@master_api_router.post("/ExpenseCategory")
async def create_expense_category(db: Session = Depends(get_db)):
    for value in ExpenseCategory:
        expense_category = models.ExpenseCategory(name=value)
        db.add(expense_category)
        db.commit()
        db.refresh(expense_category)

